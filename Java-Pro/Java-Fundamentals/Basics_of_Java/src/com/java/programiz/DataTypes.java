package com.java.programiz;

public class DataTypes {

	public static void main(String[] args) {
		
		/*.. There are 8 Primitive data types in java*/
		
		//boolean type
		boolean flag = true;
		System.out.println("Booelan "+flag);

		//byte type
		byte range = 124;
		System.out.println("byte type "+range);
		
		//Short type
		short temperature;
		temperature = -200;
		System.out.println("Short type "+temperature);
		
		//int type
		int intRange = -4250000;
		System.out.println("Int type "+intRange);
		
		//long type
		long longRange = -42332200000L;
		System.out.println("long type "+longRange);
		
		//double type
		double number = -43.8;
		System.out.println("Double type "+number);
		
		//float type
		float num = -43.8f;
		System.out.println("floating type: "+num);
		
		//char type
		char letter1 = '\u0051';
		char letter2 = 65;
		System.out.println("char type "+ letter1);
		System.out.println("char type ASCII "+letter2);
		
	}

}
